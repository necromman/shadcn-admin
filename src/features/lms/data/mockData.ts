export interface Course {
  id: string
  title: string
  instructor: string
  organization: string
  thumbnail: string
  rating: number
  reviewCount: number
  studentCount: number
  difficulty: 'beginner' | 'intermediate' | 'advanced'
  type: 'online' | 'offline' | 'blended'
  status: 'recruiting' | 'in-progress' | 'completed'
  price: number
  discountPrice?: number
  tags: string[]
  duration: string
  startDate: string
  endDate: string
  description: string
  category: string
  progress?: number
}

export interface Notice {
  id: string
  title: string
  content: string
  date: string
  category: string
  isImportant: boolean
  viewCount: number
  author: string
}

export interface Banner {
  id: string
  title: string
  subtitle: string
  description: string
  image: string
  link: string
  backgroundColor: string
  textColor: string
}

export interface Category {
  id: string
  name: string
  icon?: string
  courseCount: number
}

